#ifndef IR32_H
#define IR32_H

#include <reg52.h>

#define uchar unsigned char
#define GET_CODE32   0	 //如果想返回的是32位红外码，请定义为1，关闭定义为0

/*****************************************************************************
功能：红外接收程序初始化
返回：返回32位红外码，包含系统码
 ****************************************************************************/
void irInit(void);

/*****************************************************************************
功能：检测红外码
返回：1：有红外信号，0：无红外信号
****************************************************************************/
uchar irTest(void);

/*****************************************************************************
功能：获取8位红外数据码
返回：返回红外数据码
 ****************************************************************************/
uchar irGetDataCode(void);

/*****************************************************************************
功能：获取32位红外码，包含系统码
返回：返回32位红外码，顺序：16位系统码，8位数据码，8位数据反码
 ****************************************************************************/
unsigned long irGetAllCode(void);

/*****************************************************************************
功能：红外解码程序（外中断服务程序）
 ****************************************************************************/
void int0Isr(void);

/*****************************************************************************
功能：定时计数，红外码校验程序（定时器1中断服务程序）
 ****************************************************************************/
void time0Isr(void);

#endif
